class ScriptJobHandle(BaseModel):
    """Returned by `run_script_async` / `run_inline_script_async`.

    The script begins running in a background worker; clients poll
    `poll_script_job(job_id)` to retrieve the result once status is
    terminal (`completed` or `failed`).
    """

    program_name: str
    job_id: str = Field(..., description="opaque id for polling via poll_script_job")
    status: str = Field(
        ..., description="initial state: 'queued' or 'running' (worker may have started already)"
    )
    submitted_at: float = Field(..., description="seconds-since-epoch the job was registered")


class ScriptJobStatus(BaseModel):
    """Returned by `poll_script_job`. Result fields are populated when
    `status` is terminal (`completed` or `failed`), otherwise they're None.
    """

    job_id: str
    program_name: str
    status: str = Field(
        ..., description="lifecycle state: 'queued' | 'running' | 'completed' | 'failed'"
    )
    submitted_at: float
    started_at: float | None = Field(None, description="when the worker picked up the job")
    completed_at: float | None = Field(None, description="when the job reached a terminal state")
    stdout: str | None = None
    stderr: str | None = None
    result_repr: str | None = Field(
        None, description="repr() of the script's `result` variable, if any"
    )
    committed: bool | None = Field(
        None, description="True if the Ghidra transaction was committed"
    )
    error: str | None = Field(
        None,
        description=(
            "exception message if the script raised, or any registry-level failure"
        ),
    )


