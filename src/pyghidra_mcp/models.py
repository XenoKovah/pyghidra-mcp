from enum import Enum

from pydantic import BaseModel, Field


class DecompiledFunction(BaseModel):
    name: str
    code: str
    signature: str | None = None
    error: str | None = None
    # Rich response fields (populated when include_* flags are set)
    callees: "ListFunctionCallsResponse | None" = None
    referenced_strings: list[str] | None = None
    xrefs: list["CrossReferenceInfo"] | None = None


class DisassembledFunction(BaseModel):
    name: str
    entry: str = Field(..., description="entry-point address, e.g. '0x00401000'")
    signature: str | None = None
    listing: str = Field(
        ...,
        description=(
            "flat assembly text, one 'address: mnemonic operands ; comment' line per instruction"
        ),
    )
    instruction_count: int
    error: str | None = None


