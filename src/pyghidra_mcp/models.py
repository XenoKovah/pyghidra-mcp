class StructField(BaseModel):
    """A single field in a struct or union.

    `offset` is only honored by `create_struct_type`. For `create_union_type`
    it is ignored (unions always start each field at offset 0).
    """

    name: str
    type: str
    offset: int | None = Field(
        None, description="Explicit byte offset for struct layout; None appends"
    )


