class SearchMode(str, Enum):
    """Search mode for code search."""

    SEMANTIC = "semantic"  # Vector similarity search
    LITERAL = "literal"  # Exact string match ($contains)


class SymbolKind(str, Enum):
    """Classifications for `search_symbols`. Pass a list of values to
    narrow the search; omit or pass ``null`` to include every symbol."""

    FUNCTIONS = "functions"  # only FUNCTION symbols
    GLOBALS = "globals"  # symbols in the global namespace, excluding functions
    LABELS = "labels"  # only LABEL-type symbols (code + data labels)


class DataTypeKind(str, Enum):
    """Classifications for `search_data_types`. Pass a list of values to
    narrow the search; omit or pass ``null`` to include every kind."""

    STRUCT = "struct"
    ENUM = "enum"
    UNION = "union"
    ARRAY = "array"
    POINTER = "pointer"
    TYPEDEF = "typedef"
    FUNCTION = "function"  # function signature (prototype) data types
    PRIMITIVE = "primitive"  # Ghidra built-in types (int, char, void, ...)


class DataTypeInfo(BaseModel):
    name: str
    kind: str = Field(
        ...,
        description=(
            "classification: struct/enum/union/array/pointer/typedef/function/primitive/other"
        ),
    )
    size: int | None = Field(
        None, description="byte length; None when Ghidra reports variable/undefined length"
    )
    path: str = Field(..., description="data-type manager path, e.g. '/MyCategory/MyStruct'")


class SearchDataTypesResponse(BaseModel):
    data_types: list[DataTypeInfo]


